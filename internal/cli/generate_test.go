// TestGenerateTripwire: an output directory holding anything gqlc
// cannot prove it generated aborts the run, naming every offender in
// lexical order (subdirectories suffixed "/"), deleting nothing. A
// marked file alongside the offenders is never named and never
// touched (§5.1 steps 3-4, §5.3).
func TestGenerateTripwire(t *testing.T) {
	cases := []struct {
		name    string
		seed    func(t *testing.T, out string)
		wantWho string
	}{
		{
			name: "unmarked file",
			seed: func(t *testing.T, out string) {
				t.Helper()
				writeFixtureFile(t, filepath.Join(out, "notes.txt"), "user notes\n")
			},
			wantWho: "notes.txt",
		},
		{
			name: "subdirectory",
			seed: func(t *testing.T, out string) {
				t.Helper()
				writeFixtureFile(t, filepath.Join(out, "tx", "tx.go"), "package tx\n")
			},
			wantWho: "tx/",
		},
		{
			name: "marker-less empty file",
			seed: func(t *testing.T, out string) {
				t.Helper()
				writeFixtureFile(t, filepath.Join(out, "empty.go"), "")
			},
			wantWho: "empty.go",
		},
		{
			name: "every offender named",
			seed: func(t *testing.T, out string) {
				t.Helper()
				writeFixtureFile(t, filepath.Join(out, "helpers.go"), "package out\n")
				writeFixtureFile(t, filepath.Join(out, "tx", "tx.go"), "package tx\n")
			},
			wantWho: "helpers.go, tx/",
		},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			dir := writeProject(t)
			out := filepath.Join(dir, "out")
			writeFixtureFile(t, filepath.Join(out, "gen.go"), markedStale)
			tc.seed(t, out)
			before := snapshotDir(t, out)

			stdout, stderr, err := runGenerateIn(t, dir)
			require.Error(t, err)
			want := "output directory " + out + " contains entries not generated by gqlc: " +
				tc.wantWho + "; move or delete them and re-run"
			require.Equal(t, want, err.Error())
			require.Equal(t, "Error: "+want+"\n", stderr)
			require.Empty(t, stdout)
			require.Equal(t, before, snapshotDir(t, out), "abort must delete and modify nothing")
		})
	}
}

