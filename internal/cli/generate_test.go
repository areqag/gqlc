// TestGenerateProcsigWiring: a CALL query generates when the config
// declares a procsig registry; the identical project without the key
// fails at cypher parse with an unknown-procedure query-diag (§3.1
// stage 4) — and pins the singular "1 error" summary on the way.
func TestGenerateProcsigWiring(t *testing.T) {
	const callQuery = "// name: Labels :many\nCALL test.labels() YIELD label\nRETURN label\n"
	const registry = `{"signatures":[{"name":"test.labels","params":[],"results":[{"name":"label","type":"STRING","nullable":true}]}]}`

	t.Run("with procsig key", func(t *testing.T) {
		dir := writeProject(t)
		writeFixtureFile(t, filepath.Join(dir, "queries", "calls.cypher"), callQuery)
		writeFixtureFile(t, filepath.Join(dir, "procsig.json"), registry)
		writeFixtureFile(t, filepath.Join(dir, config.DefaultFilename), configYAML("people", "neo4j-go-v5", "procsig.json"))
		_, stderr, err := runGenerateIn(t, dir)
		require.NoError(t, err, "stderr: %s", stderr)
		require.FileExists(t, filepath.Join(dir, "out", "calls.cypher.go"))
	})

	t.Run("without procsig key", func(t *testing.T) {
		dir := writeProject(t)
		writeFixtureFile(t, filepath.Join(dir, "queries", "calls.cypher"), callQuery)
		_, stderr, err := runGenerateIn(t, dir)
		require.Error(t, err)
		require.Equal(t, "generate: 1 error", err.Error())
		require.Contains(t, stderr,
			filepath.Join(dir, "queries", "calls.cypher")+": query Labels: unknown procedure")
		require.NoDirExists(t, filepath.Join(dir, "out"))
	})
}

// TestGenerateAccumulation: one unreadable-by-grammar file plus one
// file with two failing queries plus one good file. Every diagnostic
// is present, in file-then-annotation order; the summary counts them;
// the output directory is never created (§3.3).
func TestGenerateAccumulation(t *testing.T) {
	dir := writeProject(t)
	q := filepath.Join(dir, "queries")
	writeFixtureFile(t, filepath.Join(q, "a.cypher"), "// name: Broken\nMATCH (n) RETURN n\n")
	writeFixtureFile(t, filepath.Join(q, "b.cypher"),
		"// name: BadOne :many\nMATCH (g:Ghost) RETURN g\n// name: BadTwo :many\nMATCH (w:Wraith) RETURN w\n")
	// people.cypher (the good file) is already present.

	stdout, stderr, err := runGenerateIn(t, dir)
	require.Error(t, err)
	require.Equal(t, "generate: 3 errors", err.Error())
	require.Empty(t, stdout)

	lines := strings.Split(strings.TrimSuffix(stderr, "\n"), "\n")
	require.Len(t, lines, 4)
	require.True(t, strings.HasPrefix(lines[0], filepath.Join(q, "a.cypher")+": "), "line 0: %q", lines[0])
	require.True(t, strings.HasPrefix(lines[1], filepath.Join(q, "b.cypher")+": query BadOne: "), "line 1: %q", lines[1])
	require.True(t, strings.HasPrefix(lines[2], filepath.Join(q, "b.cypher")+": query BadTwo: "), "line 2: %q", lines[2])
	require.Equal(t, "Error: generate: 3 errors", lines[3])
	require.NoDirExists(t, filepath.Join(dir, "out"))
}

