// Minimal project fixture (§7): one node type, one :many query. The
// schema name "People" mangles to package "people".
const (
	fixtureSchema = `CREATE PROPERTY GRAPH TYPE People AS {
    (:Person {
        id   :: INT64 NOT NULL,
        name :: STRING NOT NULL
    })
}
`
	fixtureQuery = "// name: AllPersons :many\nMATCH (p:Person) RETURN p\n"
)

// markedStale is a current-version marker-headed file the batch never
// emits — tripwire-passing but stale. A var: version.Version is
// ldflags-settable, not a constant.
var markedStale = "// Code generated by gqlc " + version.Version + ". DO NOT EDIT.\n\npackage stale\n"

// configYAML renders a v1 config wired to the fixture layout. procsig
// is emitted only when non-empty (the key is optional).
func configYAML(pkg, driver, procsig string) string {
	y := "version: 1\n" +
		"schema: schema.gql\n" +
		"queries: queries\n" +
		"output: out\n" +
		"package: " + pkg + "\n" +
		"schema_language: gqlc\n" +
		"query_language: opencypher\n" +
		"driver: " + driver + "\n"
	if procsig != "" {
		y += "procsig: " + procsig + "\n"
	}
	return y
}

// writeFixtureFile writes contents at path, creating parent
// directories as needed.
func writeFixtureFile(t *testing.T, path, contents string) {
	t.Helper()
	require.NoError(t, os.MkdirAll(filepath.Dir(path), 0o755))
	require.NoError(t, os.WriteFile(path, []byte(contents), 0o644))
}

// writeProject lays down the minimal project in a fresh temp dir and
// returns its root. Each test perturbs the tree it gets back.
func writeProject(t *testing.T) string {
	t.Helper()
	dir := t.TempDir()
	writeFixtureFile(t, filepath.Join(dir, "schema.gql"), fixtureSchema)
	writeFixtureFile(t, filepath.Join(dir, "queries", "people.cypher"), fixtureQuery)
	writeFixtureFile(t, filepath.Join(dir, config.DefaultFilename), configYAML("people", "neo4j-go-v5", ""))
	return dir
}

// runGenerateIn drives `gqlc generate -f <dir>/gqlc.yaml` in-process
// from the test's own working directory — deliberately not dir, so
// every run also exercises the config-dir path-resolution rule.
func runGenerateIn(t *testing.T, dir string) (string, string, error) {
	t.Helper()
	return executeRoot(t, "generate", "-f", filepath.Join(dir, config.DefaultFilename))
}

// listDir returns the directory's entry names in os.ReadDir (lexical)
// order, subdirectories suffixed "/".
func listDir(t *testing.T, dir string) []string {
	t.Helper()
	entries, err := os.ReadDir(dir)
	require.NoError(t, err)
	names := make([]string, 0, len(entries))
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() {
			name += "/"
		}
		names = append(names, name)
	}
	return names
}

// snapshotDir maps every regular file under dir (recursively) to its
// contents, for byte-identity assertions across runs.
func snapshotDir(t *testing.T, dir string) map[string]string {
	t.Helper()
	snap := make(map[string]string)
	require.NoError(t, filepath.WalkDir(dir, func(path string, d os.DirEntry, err error) error {
		require.NoError(t, err)
		if d.IsDir() {
			return nil
		}
		b, err := os.ReadFile(path)
		require.NoError(t, err)
		rel, err := filepath.Rel(dir, path)
		require.NoError(t, err)
		snap[rel] = string(b)
		return nil
	}))
	return snap
}

// requireMarkerHeaded asserts the file's first line carries both fixed
// halves of the generation marker (§5.1 step 3).
func requireMarkerHeaded(t *testing.T, path string) {
	t.Helper()
	b, err := os.ReadFile(path)
	require.NoError(t, err)
	line, _, _ := strings.Cut(string(b), "\n")
	require.True(t, strings.HasPrefix(line, "// Code generated by gqlc "), "first line of %s: %q", path, line)
	require.True(t, strings.HasSuffix(line, " DO NOT EDIT."), "first line of %s: %q", path, line)
}

func TestGenerateHappyPath(t *testing.T) {
	dir := writeProject(t)
	stdout, stderr, err := runGenerateIn(t, dir)
	require.NoError(t, err)
	require.Empty(t, stdout)
	require.Empty(t, stderr)

	out := filepath.Join(dir, "out")
	require.Equal(t, []string{"db.go", "models.go", "people.cypher.go", "querier.go"}, listDir(t, out))
	for _, name := range []string{"db.go", "models.go", "people.cypher.go", "querier.go"} {
		requireMarkerHeaded(t, filepath.Join(out, name))
	}
}

func TestGenerateDeterministic(t *testing.T) {
	dir := writeProject(t)
	_, _, err := runGenerateIn(t, dir)
	require.NoError(t, err)
	first := snapshotDir(t, filepath.Join(dir, "out"))

	_, _, err = runGenerateIn(t, dir)
	require.NoError(t, err)
	require.Equal(t, first, snapshotDir(t, filepath.Join(dir, "out")))
}

