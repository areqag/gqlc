// Marker halves of the generated-file header (render.go), matched
// version-agnostically so output written by an older gqlc release
// passes the tripwire sweep (CLI-1 spec §5.1 step 3).
const (
	markerPrefix = "// Code generated by gqlc "
	markerSuffix = " DO NOT EDIT."
)

// newGenerateCmd builds the generate command: the full config-driven
// pipeline (CLI-1 spec). SilenceUsage is set per-run in RunE so flag
// and Args parse errors keep the usage dump while runtime failures
// print diagnostics only (§2.2).
func newGenerateCmd() *cobra.Command {
	var cfgPath string
	cmd := &cobra.Command{
		Use:   "generate",
		Short: "Generate Go code from the schema and queries the config file declares",
		Long: `generate runs the pipeline the config file declares: parse the schema,
parse and resolve every named query in the query directory, and write
the generated Go package to the output directory.

The output directory belongs to gqlc alone: each run replaces its
contents, and a run aborts — deleting nothing — if the directory holds
anything gqlc cannot prove it generated.

Relative paths in the config file are relative to the config file's
directory, not the working directory.`,
		Args: cobra.NoArgs,
		RunE: func(cmd *cobra.Command, _ []string) error {
			cmd.SilenceUsage = true
			return runGenerate(cmd, cfgPath)
		},
	}
	cmd.Flags().StringVarP(&cfgPath, "file", "f", config.DefaultFilename,
		"path to the config file")
	return cmd
}

// runGenerate is the CLI adapter around pipeline.Run: it delegates
// stages 1-8 of the pipeline (spec §3.1) to internal/cli/pipeline,
// prints accumulated diagnostics, forms the summary error, and — on
// success — invokes the ADR 0012 tripwire-guarded write. The
// three-way pipeline.Result contract (see pipeline.Run doc) drives
// the three branches below.
func runGenerate(cmd *cobra.Command, cfgPath string) error {
	res, err := pipeline.Run(cfgPath)
	if err != nil {
		// Missing config file: keep the sibling-subcommand hint
		// CLI-side. The pipeline is subcommand-agnostic; the sentinel
		// is how the CLI keys off that specific stage-1 case (spec
		// §2.3).
		if errors.Is(err, pipeline.ErrConfigMissing) {
			return fmt.Errorf("no config file at %s (run gqlc init to create one)", cfgPath)
		}
		return err
	}

	// Stage-7 accumulation: nil error + populated Diagnostics. Print
	// every line to stderr in pipeline order, then return the
	// singular/plural summary (spec §2.3). Files is nil in this
	// branch; nothing is written.
	if len(res.Diagnostics) > 0 {
		for _, d := range res.Diagnostics {
			if _, werr := fmt.Fprintln(cmd.ErrOrStderr(), d); werr != nil {
				return werr
			}
		}
		noun := "errors"
		if len(res.Diagnostics) == 1 {
			noun = "error"
		}
		return fmt.Errorf("generate: %d %s", len(res.Diagnostics), noun)
	}

	// Success: write output under the ADR 0012 protocol (spec §5).
	// The pipeline resolved OutDir at stage 2 (spec §3.1) — the CLI
	// takes it verbatim rather than re-loading the config.
	return writeOutput(res.OutDir, res.Files)
}

// resolvePath joins a config-file-relative path against the config
// file's directory (CLI-1 spec §3.1 stage 2); absolute paths pass
// through unchanged. Shared with init.go (warning-line path checks);
// pipeline.Run has its own internal copy for stage 2.
func resolvePath(baseDir, p string) string {
	if filepath.IsAbs(p) {
		return p
	}
	return filepath.Join(baseDir, p)
}

// writeOutput is the §5.1 tripwire algorithm, steps 1-6 in order. It
// runs only after Generate returned its []File, so every earlier
// failure wipes nothing and writes nothing (§5.2).
func writeOutput(outDir string, files []codegen.File) error {
	// Step 1 — stat.
	info, err := os.Stat(outDir)
	switch {
	case errors.Is(err, fs.ErrNotExist):
		if err := os.MkdirAll(outDir, 0o755); err != nil {
			return fmt.Errorf("output: %w", err)
		}
		return writeFiles(outDir, files)
	case err != nil:
		return fmt.Errorf("output: %w", err)
	case !info.IsDir():
		return fmt.Errorf("output: %s is not a directory", outDir)
	}

	// Step 2 — read.
	entries, err := os.ReadDir(outDir)
	if err != nil {
		return fmt.Errorf("output: %w", err)
	}

	// Step 3 — sweep every entry, collecting every offender, not the
	// first (ADR 0012: the abort names them all). A read failure
	// aborts: the directory's state cannot be proven, so nothing is
	// touched.
	var offenders []string
	for _, e := range entries {
		if e.IsDir() {
			offenders = append(offenders, e.Name()+"/")
			continue
		}
		if !e.Type().IsRegular() {
			offenders = append(offenders, e.Name())
			continue
		}
		marked, err := markedFile(filepath.Join(outDir, e.Name()))
		if err != nil {
			return fmt.Errorf("output: %w", err)
		}
		if !marked {
			offenders = append(offenders, e.Name())
		}
	}

	// Step 4 — offenders present → abort (§5.3). Nothing is deleted.
	if len(offenders) > 0 {
		return fmt.Errorf("output directory %s contains entries not generated by gqlc: %s; move or delete them and re-run",
			outDir, strings.Join(offenders, ", "))
	}

	// Step 5 — wipe: after step 3 every entry is a proven-marked
	// file. The directory itself is kept.
	for _, e := range entries {
		if err := os.Remove(filepath.Join(outDir, e.Name())); err != nil {
			return fmt.Errorf("output: %w", err)
		}
	}

	return writeFiles(outDir, files)
}

// writeFiles is step 6: each File lands at its Path in slice order —
// Generate's contract sorts the slice by Path, so write order is
// deterministic too.
func writeFiles(outDir string, files []codegen.File) error {
	for _, f := range files {
		if err := os.WriteFile(filepath.Join(outDir, f.Path), f.Contents, 0o644); err != nil {
			return fmt.Errorf("output: %w", err)
		}
	}
	return nil
}

// markedFile reports whether the file's first line — the bytes before
// the first LF; a file without an LF is its own first line — starts
// with markerPrefix and ends with markerSuffix. Version-agnostic in
// the middle: checking against the current version.Version would make
// every gqlc upgrade brick its own output directory (§5.1).
func markedFile(path string) (bool, error) {
	contents, err := os.ReadFile(path)
	if err != nil {
		return false, err
	}
	line, _, _ := bytes.Cut(contents, []byte("\n"))
	return bytes.HasPrefix(line, []byte(markerPrefix)) && bytes.HasSuffix(line, []byte(markerSuffix)), nil
}
